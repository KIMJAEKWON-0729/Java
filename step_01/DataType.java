
public class DataType {

	public static void main(String[] args) {
		String name = "홍길동";
		int age = 26;
		double weight = 75.6;
		float tall= 175.8f;
		char gender = '남';
		System.out.print("나의 이름은 "+name+"입니다\n");
		System.out.println("나이는  "+ age + " 세  입니다");
		System.out.println("체중은  "+ weight + " Kg입니다");
		System.out.println("키는"+ tall + "입니다");
		System.out.println("성별은 " + gender + " 입니다");
	}

}

//double 은 소수 같다  데이터의 자료형 잘 따져야지 
