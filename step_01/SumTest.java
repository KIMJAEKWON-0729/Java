public class SumTest {
	public static void main(String[] args) {
		int a = 5, b = 3;
		int c = a+b;
		System.out.println(5+3);
		System.out.println(a+b);
		System.out.println("5+3="+c);
		System.out.println(a+"+"+b+"="+c);
	}

}
