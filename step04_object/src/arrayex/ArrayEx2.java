package arrayex;
// 2차원 배열
public class ArrayEx2 {
	public static void main(String[] args) {
		int [][]num = new int[][] {{10,40,60,100},{20,50,40,11},{40,3,4,2}};
		for (int i = 0; i < num.length; i++) {
			for (int j = 0; j < 4; j++){
				System.out.println(num[i][j]);
				
			}
			
		}
		}
}
