
public class SwitchBasic {
	public static void main(String[] args) {
		int n = 3;
		switch (n) {
		case 1:
			System.out.println("1번");
			break;
		case 2:
			System.out.println("2번");
			break;
		case 3:
			System.out.println("3번");
			break;	
		default:
			System.out.println("defalut 출력");
		}
	}
}
